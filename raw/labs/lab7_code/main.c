void bit_rev(complex *X, uint16_t size)
{
    uint16_t i,j,k;

    complex temp;	/* temporary storage of the complex variable */

    j = 0;
    for (i=1;i<size-1;i++)
    {

    	k = size>>1; // size/2
        while(k<=j)
        {
           	j-=k;
            k>>=1; // k = k/2
        }
        j+=k;

        if (i<j) // swap
        {
          temp = X[j];
          X[j] = X[i];
          X[i] = temp;
        }
    }
}

