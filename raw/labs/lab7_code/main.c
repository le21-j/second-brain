	  if (SCALE) {
		  X[i].re = X[i].re*SIZE_FFT;
		  X[i].im = X[i].im*SIZE_FFT;
		  X_fixed_point_to_float[i].re = X_fixed_point_to_float[i].re*SIZE_FFT;
		  X_fixed_point_to_float[i].im = X_fixed_point_to_float[i].im*SIZE_FFT;
	  }

